package br.com.fiap.traveller.entretenimento.servlet;

public class Entretenimento {
	
	private int id;
	private String nome;
	private String endereco;
	private int classificacao;
	private int hrfuncionamento;
	private int valor;
	private String site;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getEndereco() {
		return endereco;
	}
	public void setEndereco(String endereco) {
		this.endereco = endereco;
	}
	public int getClassificacao() {
		return classificacao;
	}
	public void setClassificacao(int classificacao) {
		this.classificacao = classificacao;
	}
	public int getHrfuncionamento() {
		return hrfuncionamento;
	}
	public void setHrfuncionamento(int hrfuncionamento) {
		this.hrfuncionamento = hrfuncionamento;
	}
	public int getValor() {
		return valor;
	}
	public void setValor(int valor) {
		this.valor = valor;
	}
	public String getSite() {
		return site;
	}
	public void setSite(String site) {
		this.site = site;
	}
	
	
}
